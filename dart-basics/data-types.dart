void main() {

  // Strings
  String firstName = "Anna";
  print("String: $firstName");

  // Integers
  int intNum = 33;
  print("Integer: $intNum");

  // Doubles
  double doubleNum = 33.33;
  print("Double: $doubleNum");

  // Booleans
  bool myFalseBool = false;
  bool myTrueBool = true;

  print("False Boolean: $myFalseBool");
  print("True Boolean: $myTrueBool");

  // Dynamic
  dynamic fullName = "Anna Chandra";
  print("Dynamic: $fullName");

  // Other Data Types are Lists and Maps
}